package com.jmjk.pojo;

import java.util.ArrayList;

public class Ids {

	private ArrayList<healthFileId> healthFileIds;

	@Override
	public String toString() {
		return "Ids [healthFileIds=" + healthFileIds + "]";
	}

	public Ids() {
		super();
	}

	public Ids(ArrayList<healthFileId> healthFileIds) {
		super();
		this.healthFileIds = healthFileIds;
	}

	public ArrayList<healthFileId> getHealthFileIds() {
		return healthFileIds;
	}

	public void setHealthFileIds(ArrayList<healthFileId> healthFileIds) {
		this.healthFileIds = healthFileIds;
	}

	public class healthFileId {

		public int healthfileid;

		@Override
		public String toString() {
			return "healthFileId [healthfileid=" + healthfileid + "]";
		}

		public healthFileId() {
			super();
		}

		public healthFileId(int healthfileid) {
			super();
			this.healthfileid = healthfileid;
		}

		public int getHealthfileid() {
			return healthfileid;
		}

		public void setHealthfileid(int healthfileid) {
			this.healthfileid = healthfileid;
		}
	}
}
