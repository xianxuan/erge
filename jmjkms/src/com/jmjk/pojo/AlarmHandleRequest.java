package com.jmjk.pojo;
/**
 *  预警处理请求数据
 * @author 赖清发
 *
 */
public class AlarmHandleRequest {
	public AlarmHandleRequest() {
	      
	}
	
	public AlarmHandleRequest(String appKey, int id, String appUserID,
			String meaTime, String devID, int level, String alarmTime,
			int alarmType, String alarmQuota, int status) {
		super();
		this.appKey = appKey;
		this.id = id;
		this.appUserID = appUserID;
		this.meaTime = meaTime;
		this.devID = devID;
		this.level = level;
		this.alarmTime = alarmTime;
		this.alarmType = alarmType;
		this.alarmQuota = alarmQuota;
		this.status = status;
	}

	private String appKey;    //对接系统Key
	private int id;           //序号
	private String appUserID; //对接系统用户ID
	private String meaTime;
	private String devID;
	private int level;
	private String alarmTime;
	private int alarmType;
	private String alarmQuota;
	private int status;
	
	
	
	
	public String getAppKey() {
		return appKey;
	}
	public void setAppKey(String appKey) {
		this.appKey = appKey;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAppUserID() {
		return appUserID;
	}
	public void setAppUserID(String appUserID) {
		this.appUserID = appUserID;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getMeaTime() {
		return meaTime;
	}
	public void setMeaTime(String meaTime) {
		this.meaTime = meaTime;
	}
	public String getDevID() {
		return devID;
	}
	public void setDevID(String devID) {
		this.devID = devID;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public String getAlarmTime() {
		return alarmTime;
	}
	public void setAlarmTime(String alarmTime) {
		this.alarmTime = alarmTime;
	}
	public int getAlarmType() {
		return alarmType;
	}
	public void setAlarmType(int alarmType) {
		this.alarmType = alarmType;
	}
	public String getAlarmQuota() {
		return alarmQuota;
	}
	public void setAlarmQuota(String alarmQuota) {
		this.alarmQuota = alarmQuota;
	}
	
}
